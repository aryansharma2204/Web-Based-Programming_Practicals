<?php

// For loop example
echo "For loop:\n";
for ($i = 1; $i <= 5; $i++) {
    echo $i . " ";
}
echo "\n\n";

// While loop example
echo "While loop:\n";
$j = 1;
while ($j <= 5) {
    echo $j . " ";
    $j++;
}
echo "\n\n";

// Do-while loop example
echo "Do-while loop:\n";
$k = 1;
do {
    echo $k . " ";
    $k++;
} while ($k <= 5);
echo "\n";

?>
